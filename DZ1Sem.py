# 1.Напишите программу, которая принимает на вход цифру, обозначающую день
# недели, и проверяет, является ли этот день выходным.
#
# Пример:
#
# - 6 -> да
# - 7 -> да
# - 1 -> нет
def task1(number):
    days_of_week = {1: 'Понедельник',
                    2: 'Вторник',
                    3: 'Среда',
                    4: 'Четверг',
                    5: 'Пятница',
                    6: 'Суббота',
                    7: 'Воскресенье'}
    weekends = [6, 7]
    if number < 1 or number > 7:
        print(f'{number} -> Нет такого дня недели')
    elif number in weekends:
        print(f'{number} -> {days_of_week[number]} выходной день')
    else:
        print(f'{number} -> {days_of_week[number]} будний день')


