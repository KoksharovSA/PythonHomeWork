# причём X ≠ 0 и Y ≠ 0 и выдаёт номер четверти плоскости, в которой
# находится эта точка
# (или на какой оси она находится).
#
# Пример:
#
# - x=34; y=-30 -> 4
# - x=2; y=4-> 1
# - x=-34; y=-30 -> 3
def task3(x, y):
    result = {x > 0 and y > 0: 'в 1 четверти.',
              x < 0 and y > 0: 'во 2 четверти.',
              x < 0 and y < 0: 'в 3 четверти.',
              x > 0 and y < 0: 'в 4 четверти.',
              x == 0 and y == 0: 'в начале координат.',
              (x == 0 or y == 0) and not (
                      x == 0 and y == 0): f'на оси {"x" if x == 0 else "y"}.'}
    print(f'Координата ({x},{y}) находится {result[True]}')


