# 3)Формат входных данных
# В первой строке подаётся число n
# n – количество холодильников. В последующих
# n строках вводятся строки, содержащие латинские строчные буквы и цифры, в каждой строке от
# 5 до 100 символов.
# Формат выходных данных
# Программа должна вывести номера зараженных холодильников через пробел. Если таких холодильников нет, ничего выводить не нужно.
# Sample Input 1:
# 6
# 222anton456
# a1n1t1o1n1
# 0000a0000n00t00000o000000n
# gylfole
# richard
# ant0n
# Sample Output 1:
# 1 2 3

def task3(n):
    list_ser_nom = []
    while n != 0:
        list_ser_nom.append(input('Введите серийный номер: '))
        n -= 1
    result = [''.join([x for x in ser_nom if not x.isdigit()]) for
              ser_nom in list_ser_nom]
    print(result)
task3(2)

