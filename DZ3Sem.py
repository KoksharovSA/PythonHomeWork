# 1.Задайте список из нескольких чисел. Напишите программу, которая найдёт
# сумму элементов списка, стоящих на нечётной позиции.
# Пример:
# - [2, 3, 5, 9, 3] -> на нечётных позициях элементы 3 и 9, ответ: 12

def task1(list_elements):
    print(sum([x for i, x in enumerate(list_elements) if i % 2 != 0]))
    
task1([2, 3, 5, 9, 3])

# 2.Напишите программу, которая найдёт произведение пар чисел списка.
# Парой считаем первый и последний элемент, второй и предпоследний и т.д.
# Пример:
# - [2, 3, 4, 5, 6] => [12, 15, 16];
# - [2, 3, 5, 6] => [12, 15]
from math import ceil


def task2(list_elements):
    print([list_elements[x] * list_elements[-(x+1)] for x in range(ceil(len(
        list_elements)/2))])
    
task2([2, 3, 4, 5, 6])
task2([2, 3, 5, 6])


# 3.Задайте список из вещественных чисел. Напишите программу, которая найдёт
# разницу между максимальным и минимальным значением дробной части элементов.
# Пример:
# - [1.1, 1.2, 3.1, 5, 10.01] => 0.19

def task3(list_elements):
    list_remainders = [(x-int(x)) for x in list_elements]
    print(max(list_remainders)-min(list_remainders))
    
task3([1.1, 1.2, 3.1, 5, 10.01])

# 4.Напишите программу, которая будет преобразовывать десятичное число в
# двоичное.
# Пример:
# - 45 -> 101101
# - 3 -> 11
# - 2 -> 10

def task4(n):
    #print(bin(n))
    #или так
    binary_n = ''
    while n > 0:
        binary_n = str(n % 2) + binary_n
        n = n // 2
    print(binary_n)

